public class TagInfo
{
	private int TagId;
	private String Description;
	
	public int getTagId()
	{
		return TagId;
	}
	
	public String getDescription()
	{
		return Description;
	}
	
	public void setTagId(int TagId)
	{
		this.TagId = TagId;
	}
	
	public void setDescription(String Description)
	{
		this.Description = Description;
	}
}